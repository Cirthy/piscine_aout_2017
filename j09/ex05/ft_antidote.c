int	ft_antidote(int i, int j, int k)
{
	if (i <= j)
	{
		if (k >= j)
			return (j);
		if (k <= i)
			return (i);
		return (k);
	}
	if (k >= i)
		return (i);
	if (k <= j)
		return (j);
	return (k);
}
