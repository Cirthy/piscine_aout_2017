char*ft_rot42(char *str)
{
	int i;

	i = -1;
	while (str[++i])
	{
		if (('a' <= str[i] && str[i] <= 'z') ||
			('A' <= str[i] && str[i] <= 'Z'))
		{
			if (str[i] <= 'J' || ('a' <= str[i] && str[i] <= 'j'))
				str[i] += 16;
			else
				str[i] -= 10;
		}
	}
	return (str);
}
